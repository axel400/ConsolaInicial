            string estatura = Console.ReadLine();
            float estatura1 = float.Parse(estatura);
            Console.Write("Ingrese la fecha que estas iniciando: ");
            var InicioPartida = Console.ReadLine();
            Console.Write("Ingresa un sobrenombre para tu personaje : ");
            var SobreNombre = Console.ReadLine();
            Console.Write("Ingrese el numero de peleas que quieres tener : ");
            string Peleas = Console.ReadLine();
            int Peleas1 = int.Parse(Peleas);
            Console.Write("Ingrese una frase motivacional : ");
            var Frase = Console.ReadLine();

            var persona = new HunterxHunter();

            persona.Habilidad = Habilidad1;
            persona.nombre = nombre;
            persona.TipoPersonaje = TipoPersonaje;
            persona.Poderes = Poderes1;
